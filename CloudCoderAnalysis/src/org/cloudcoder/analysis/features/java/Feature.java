package org.cloudcoder.analysis.features.java;

/**
 * @author Andrei Papancea
 *
 * Object representation of a feature
 * 
 */

public class Feature {

	private String name;
	private int id;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
}
